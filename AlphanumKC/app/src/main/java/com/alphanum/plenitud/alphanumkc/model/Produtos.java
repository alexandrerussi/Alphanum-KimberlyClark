package com.alphanum.plenitud.alphanumkc.model;

public class Produtos {

    private String idProduto;
    private String nomeProduto;
    private String pontosProduto;
    private String precoProd;
    private String qtdFraldas;
    private String tamanhoFraldas;

    public Produtos(){

    }

    public String getIdProduto() {
        return idProduto;
    }

    public void setIdProduto(String idProduto) {
        this.idProduto = idProduto;
    }

    public String getNomeProduto() {
        return nomeProduto;
    }

    public void setNomeProduto(String nomeProduto) {
        this.nomeProduto = nomeProduto;
    }

    public String getPontosProduto() {
        return pontosProduto;
    }

    public void setPontosProduto(String pontosProduto) {
        this.pontosProduto = pontosProduto;
    }

    public String getPrecoProd() {
        return precoProd;
    }

    public void setPrecoProd(String precoProd) {
        this.precoProd = precoProd;
    }

    public String getQtdFraldas() {
        return qtdFraldas;
    }

    public void setQtdFraldas(String qtdFraldas) {
        this.qtdFraldas = qtdFraldas;
    }

    public String getTamanhoFraldas() {
        return tamanhoFraldas;
    }

    public void setTamanhoFraldas(String tamanhoFraldas) {
        this.tamanhoFraldas = tamanhoFraldas;
    }
}
